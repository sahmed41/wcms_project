    private void cmb_idItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmb_idItemStateChanged
        // TODO add your handling code here:
        String id = String.valueOf(cmb_id.getSelectedItem());
        String latitude;
        String longitude;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver"); 
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/wcms","wcms","e3H$l5A@eN^p7T&");
            String sql = "SELECT location FROM habitat WHERE id = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, String.valueOf(id));
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                String location[] = rs.getString("location").split(",");
                latitude = location[0].replaceAll("\\s", "");;
                longitude = location[1].replaceAll("\\s", "");;
                txt_latitude.setText(latitude);
                txt_longitude.setText(longitude);
            } 
//                System.out.println(rs.getString("location"));

            conn.close();
        } catch (Exception e) {
            lbl_error_message.setText("Failiure: " + e);
        }
        
    }//GEN-LAST:event_cmb_idItemStateChanged

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        lbl_error_message.setText("");
        if ( String.valueOf(cmb_id.getSelectedItem()).equals("Select") ||
                txt_latitude.getText().equals("") || 
                !Validator.isNumber(txt_latitude.getText()) ||
                txt_longitude.getText().equals("") ||
                !Validator.isNumber(txt_longitude.getText()) ) {
            lbl_error_message.setText("Lattitude and longitude are required to add habitat as numbers");
            lbl_error_message_1.setText("and a valid ID should be picked");
        } else { 
            Habitat h = new Habitat();
            h.setId(String.valueOf(cmb_id.getSelectedItem()));
            h.setLocation(txt_latitude.getText() + ", "+ txt_longitude.getText() );
            h.setWpa(getWpa());
            h.edit();
            JOptionPane.showMessageDialog(this, "Habitat successfully Deleted!");
            WildlifeRangerDashboard wrd = new WildlifeRangerDashboard(getRangerId(),getUsername(), getPassword(), "Wildlife Ranger");
            wrd.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_btn_editActionPerformed

